def load_json(path):
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)


def save_json(path, data):
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write('\n')


def sort_translation_keys(data):
    """Sort the translation keys alphabetically, case-insensitive."""
    translation = data.get('translation', {})
    sorted_keys = sorted(translation.keys(), key=lambda k: k.lower())
    sorted_translation = {k: translation[k] for k in sorted_keys}
    result = {}
    # Preserve any top-level keys other than 'translation'
    for k in data:
        if k != 'translation':
            result[k] = data[k]
    result['translation'] = sorted_translation
    return result


def main():
    base_dir = os.path.dirname(os.path.abspath(__file__))
    locales_dir = os.path.join(base_dir, LOCALES_DIR)

    added_total = 0
    for lang in LANGS:
        path = os.path.join(locales_dir, f'{lang}.json')
        if not os.path.exists(path):
            print(f'  SKIP {lang}: file not found at {path}')
            continue

        data = load_json(path)
        translation = data.get('translation', {})
        added = 0

        for key, translations in KEYS.items():
            if key not in translation:
                translation[key] = translations.get(lang, key)
                added += 1

        if added > 0:
            data['translation'] = translation
            # Sort alphabetically
            data = sort_translation_keys(data)
            save_json(path, data)
            print(f'  {lang}: +{added} keys')
        else:
            print(f'  {lang}: no new keys')

        added_total += added

    print(f'\nTotal new keys added: {added_total}')


if __name__ == '__main__':
    main()
